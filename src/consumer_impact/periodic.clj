(defrecord Category [description])
(def categories
  {:diatomic_nonmetal (->Category "Diatomic nonmetal")
   :noble_gas (->Category "Noble gas")
   :polyatomic_nonmetal (->Category "Polyatomic nonmetal")
   :superactinide (->Category "Superactinide")
